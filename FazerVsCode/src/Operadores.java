public class Operadores {
    public float adicao (float x, float y)
    {
        return x + y;
    }

    public float subt (float x, float y)
    {
        return x - y;
    }

    public float multi (float x, float y)
    {
        return x - y;
    }

    public float divis (float x, float y)
    {
        return x - y;
    }
}
